-- DROP TABLE measured_foot;
CREATE TABLE measured_foot (
measured_person_id                serial          NOT NULL,
name                             VARCHAR(100)    NOT NULL,
kana                             VARCHAR(100)    NOT NULL,
kind                           VARCHAR(10)         NOT NULL,
age                              INTEGER         NOT NULL,
hight                            INTEGER         NOT NULL,
weight                           INTEGER         NOT NULL,
regular_shoe_size                DECIMAL         NOT NULL,
measured_date                    VARCHAR(15)            NOT NULL,
writtenby_person                 VARCHAR(100)    NOT NULL,
left_length                      INTEGER         NOT NULL,
left_weighted_thickness          INTEGER         NOT NULL,
left_regular_thickness           INTEGER         NOT NULL,
left_weighted_width              INTEGER         NOT NULL,
left_regular_width               INTEGER         NOT NULL,
right_length                     INTEGER         NOT NULL,
right_weighted_thickness         INTEGER         NOT NULL,
right_regular_thickness          INTEGER         NOT NULL,
right_weighted_width             INTEGER         NOT NULL,
right_regular_width              INTEGER         NOT NULL,
result_left_weighted_thickness   VARCHAR(5)         NOT NULL,
result_left_regular_thickness    VARCHAR(5)         NOT NULL,
result_left_weighted_width       VARCHAR(5)         NOT NULL,
result_left_regular_width        VARCHAR(5)         NOT NULL,
result_right_weighted_thickness  VARCHAR(5)         NOT NULL,
result_right_regular_thickness   VARCHAR(5)         NOT NULL,
result_right_weighted_width      VARCHAR(5)         NOT NULL,
result_right_regular_width       VARCHAR(5)         NOT NULL,
hardness                         VARCHAR(10)    NOT NULL,
recomend_shoe_size                             DECIMAL         NOT NULL,
thickness                        VARCHAR(10)         NOT NULL,
PRIMARY KEY (measured_person_id)
);
